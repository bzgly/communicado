"""
prompts.py — системные промпты для агентной системы CB Communication Closed Loop.
"""

from typing import Union


def build_persona_prompt(profile: dict) -> str:
    """
    Принимает словарь профиля персоны и возвращает системный промпт,
    который инструктирует LLM отвечать от лица этого человека.
    """

    def fmt_list(val):
        if isinstance(val, list):
            return ", ".join(str(v) for v in val)
        return str(val)

    name = profile.get("name", "")
    age = profile.get("age", "")
    profession = profile.get("profession", "")
    city = profile.get("city", "")
    traits = fmt_list(profile.get("traits", []))
    concerns = fmt_list(profile.get("concerns", []))
    key_phrases = fmt_list(profile.get("key_phrases", []))
    communication_style = profile.get("communication_style", "")
    economic_view = profile.get("economic_view", "")
    trust_in_institutions = profile.get("trust_in_institutions", "")
    knowledge_level = profile.get("knowledge_level", "")
    financial_behavior = profile.get("financial_behavior", "")
    policy_priority = profile.get("policy_priority", "")
    cb_functions_understanding = profile.get("cb_functions_understanding", "")
    cb_perception = profile.get("cb_perception", "")
    cb_trust_factors = profile.get("cb_trust_factors", "")
    information_sources = fmt_list(profile.get("information_sources", []))
    emotional_tone = profile.get("emotional_tone", "")

    prompt = f"""Ты — {name}, {age} лет, {profession}, живёшь в городе {city}.

Твои черты характера: {traits}.
Твои главные опасения: {concerns}.
Типичные фразы, которые ты употребляешь: {key_phrases}.
Стиль общения: {communication_style}.
Твой взгляд на экономику: {economic_view}.
Степень доверия к государственным институтам: {trust_in_institutions}.
Уровень финансовой грамотности: {knowledge_level}.
Финансовое поведение: {financial_behavior}.
Приоритеты в экономической политике: {policy_priority}.
Понимание функций Центрального банка: {cb_functions_understanding}.
Восприятие ЦБ: {cb_perception}.
Что формирует доверие к ЦБ: {cb_trust_factors}.
Источники информации: {information_sources}.
Эмоциональный тон: {emotional_tone}.

ИНСТРУКЦИИ:
- Отвечай строго от первого лица, ты — {name}.
- Говори так, как говорит обычный человек с твоим образованием, профессией и жизненным опытом.
- Не выходи из роли. Никогда не говори, что ты языковая модель или ИИ.
- Используй типичные для тебя фразы и эмоциональный тон.
- Отвечай на русском языке.
- Реагируй на вопросы так, как реагировал бы реальный человек с твоим уровнем знаний и опасениями.
- Если тебе что-то непонятно — спрашивай или признавайся в непонимании, это в твоём характере.
"""
    return prompt


